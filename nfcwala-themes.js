// Apply theme function
export function applyColorTheme(themeId, isDarkMode) {
    const theme = colorThemes.find(t => t.id === themeId);
    if (!theme) return;
    
    const gradient = isDarkMode ? theme.gradient : theme.lightGradient;
    const primary = isDarkMode ? theme.primary : theme.lightPrimary;
    
    document.documentElement.style.setProperty('--color-accent', primary);
    document.documentElement.style.setProperty('--color-accent-gradient', gradient);
    
    localStorage.setItem('yashAI_theme', themeId);
}
